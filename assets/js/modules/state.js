/* modules/state.js — single source of truth + localStorage I/O (v4) */
window.SHIBI = window.SHIBI || {};
window.SHIBI.State = (function () {

  var V1_KEY = 'shibi.dashboard.v1';
  var V2_KEY = 'shibi.dashboard.v2';
  var V3_KEY = 'shibi.dashboard.v3';
  var V4_KEY = 'shibi.dashboard.v4';

  function defaultState() {
    return {
      streak: 0,
      lastActiveDate: null,
      joinDate: SHIBI.Time.todayStr(),
      hoursToday: 0,
      hoursDate: SHIBI.Time.todayStr(),
      weeklyHours: [0, 0, 0, 0, 0, 0, 0],
      weeklyHoursDate: SHIBI.Time.weekStartStr(),

      trackers: {
        java:       { completed: [] },
        dsa:        { completed: [] },
        cyber:      { completed: [] },
        aptitude:   { completed: [] },
        web:        { completed: [] },
        networking: { completed: [] }
      },

      dsaSolved: 0,
      dsaToday: 0,
      dsaTodayDate: SHIBI.Time.todayStr(),
      dsaStreak: 0,

      thmLabs: 0,
      miniProjects: 0,

      aptWeek: 0,
      aptWeekDate: SHIBI.Time.weekStartStr(),

      tasks:  [],
      goals:  [],
      notes:  '',
      repos:  [],

      pomoToday: 0,
      pomoTodayDate: SHIBI.Time.todayStr(),
      pomoTotal: 0,

      earnedBadges: [],
      xp: 0,
      level: 1,
      xpHistory: [],  // [{ts, amount, reason}]

      monthlyTargetsDone: {},
      weeklyTargetsDone: {},
      roadmapDaysDone: {},
      roadmapTasksDone: {},

      quizHistory: [],

      // v3: daily activity for heatmap
      dailyActivity: {},  // { "YYYY-MM-DD": { hours, problemsSolved, tasksDone, score } }

      // v3/v4: placement countdown + custom date system
      placement: {
        targetDate:       null,   // legacy v3 mirror → kept in sync with endDate
        totalDays:        90,     // deprecated — computed via Timetable.totalDays()
        targetHoursPerDay: 6,
        // v4 new fields:
        startDate:    null,       // ISO "YYYY-MM-DD"
        endDate:      null,       // ISO "YYYY-MM-DD" (mirrors targetDate)
        setupComplete: false,
        priority:     'balanced', // 'placement_focus'|'cybersec_focus'|'balanced'
        weakSubjects: [],         // subjects to boost
        goals:        []          // optional weighted goals
      },

      // v4: dynamic timetable (generated by timetableEngine.js)
      timetable: {
        generatedAt:   null,
        days:          {},   // { "YYYY-MM-DD": { dayIndex, phase, tasks[], subjects{}, estimatedHours, done, completedTasks[] } }
        weeks:         [],   // [{ weekNum, startDate, endDate, items[], hoursTarget }]
        months:        [],   // [{ monthNum, startDate, endDate, milestones[] }]
        _prevSnapshot: null, // for undo of auto-adjust
        mode:          'standard',
        lastAdjusted:  null,
        adjustedCount: 0
      },

      // v3: interview prep
      interviewPracticed: {},  // { questionId: true }
      mockInterviews: [],       // [{date, type, feedback, score/10}]

      // v3: company tracker
      companyProgress: {},      // { companyId: { topicsDone: [] } }

      // v3: cyber labs
      labsDone: {},             // { labId: { done, notes, completedDate } }

      // v3: daily coding challenge
      dailyChallenges: {},      // { "YYYY-MM-DD": { easy: bool, med: bool, hard: bool } }
      dailyChallengeStreak: 0,
      dailyChallengeLast: null,

      // v3: resume
      resume: {
        name: '', email: '', phone: '', github: '', linkedin: '',
        summary: '',
        education: [],      // [{degree, institution, year, cgpa}]
        skills: [],         // ['Java', 'Python', ...]
        projects: [],       // [{name, description, tech, link}]
        certifications: [], // [{name, issuer, year}]
        experience: []      // [{role, company, duration, description}]
      },

      // v3: notes vault
      notesVault: [],  // [{id, title, category, body, tags, pinned, createdTs, updatedTs}]

      // v3: focus sessions
      focusSessions: [],  // [{startTs, durationMs, endedAt}]

      settings: {
        accent:        '#00ffd0',
        matrix:        true,
        particles:     true,
        light:         false,
        cardStyle:     'glass',
        fontScale:     'default',
        reducedMotion: false,
        bgDim:         0.18,
        compactMode:   false,
        contentLang:   'en',        // v4: 'en' | 'ml'
        showLangToggles: true       // v4: show per-block translate buttons
      }
    };
  }

  function migrateOld(oldParsed) {
    var fresh = defaultState();
    var copyFields = [
      'streak', 'lastActiveDate', 'joinDate',
      'hoursToday', 'hoursDate', 'weeklyHours', 'weeklyHoursDate',
      'dsaSolved', 'dsaToday', 'dsaTodayDate', 'dsaStreak',
      'thmLabs', 'miniProjects',
      'aptWeek', 'aptWeekDate',
      'tasks', 'goals', 'notes', 'repos',
      'pomoToday', 'pomoTodayDate', 'pomoTotal',
      'earnedBadges', 'xp', 'level', 'xpHistory',
      'monthlyTargetsDone', 'weeklyTargetsDone',
      'roadmapDaysDone', 'roadmapTasksDone',
      'quizHistory',
      'dailyActivity', 'placement',
      'interviewPracticed', 'mockInterviews',
      'companyProgress', 'labsDone',
      'dailyChallenges', 'dailyChallengeStreak', 'dailyChallengeLast',
      'resume', 'notesVault', 'focusSessions',
      'timetable'
    ];
    copyFields.forEach(function (k) {
      if (oldParsed[k] !== undefined) fresh[k] = oldParsed[k];
    });
    if (oldParsed.settings) Object.assign(fresh.settings, oldParsed.settings);
    // v4: ensure placement has new fields (v3 data may not have them)
    if (fresh.placement) {
      if (!fresh.placement.startDate && fresh.placement.targetDate)
        fresh.placement.endDate = fresh.placement.targetDate; // migrate targetDate → endDate
      if (!fresh.placement.priority)     fresh.placement.priority     = 'balanced';
      if (!fresh.placement.weakSubjects) fresh.placement.weakSubjects = [];
      if (!fresh.placement.goals)        fresh.placement.goals        = [];
      if (fresh.placement.setupComplete === undefined) fresh.placement.setupComplete = false;
    }
    if (oldParsed.trackers) {
      ['java', 'dsa', 'cyber', 'aptitude', 'web', 'networking'].forEach(function (k) {
        if (oldParsed.trackers[k]) fresh.trackers[k] = oldParsed.trackers[k];
      });
    }
    return fresh;
  }

  // Deep-merge saved into defaults so new nested fields from defaultState() always exist
  function deepMerge(defaults, saved) {
    var result = Object.assign({}, defaults);
    for (var key in saved) {
      if (!Object.prototype.hasOwnProperty.call(saved, key)) continue;
      var sv = saved[key];
      var dv = defaults ? defaults[key] : undefined;
      if (sv !== null && typeof sv === 'object' && !Array.isArray(sv) &&
          dv !== null && typeof dv === 'object' && !Array.isArray(dv)) {
        result[key] = deepMerge(dv, sv);
      } else {
        result[key] = sv;
      }
    }
    return result;
  }

  function load() {
    // try v4 first
    try {
      var raw4 = localStorage.getItem(V4_KEY);
      if (raw4) {
        var parsed4 = JSON.parse(raw4);
        return deepMerge(defaultState(), parsed4);
      }
    } catch (e) {}

    // migrate from v3
    try {
      var raw3 = localStorage.getItem(V3_KEY);
      if (raw3) {
        var migrated3 = migrateOld(JSON.parse(raw3));
        localStorage.setItem(V4_KEY, JSON.stringify(migrated3));
        return migrated3;
      }
    } catch (e) {}

    // migrate from v2
    try {
      var raw2 = localStorage.getItem(V2_KEY);
      if (raw2) {
        var migrated = migrateOld(JSON.parse(raw2));
        localStorage.setItem(V4_KEY, JSON.stringify(migrated));
        return migrated;
      }
    } catch (e) {}

    // migrate from v1
    try {
      var raw1 = localStorage.getItem(V1_KEY);
      if (raw1) {
        var migrated1 = migrateOld(JSON.parse(raw1));
        localStorage.setItem(V4_KEY, JSON.stringify(migrated1));
        return migrated1;
      }
    } catch (e) {}

    return defaultState();
  }

  function save(s) {
    localStorage.setItem(V4_KEY, JSON.stringify(s));
  }

  function reset() {
    [V4_KEY, V3_KEY, V2_KEY, V1_KEY].forEach(function (k) { localStorage.removeItem(k); });
    location.reload();
  }

  function dailyReset(s) {
    var today = SHIBI.Time.todayStr();
    var week  = SHIBI.Time.weekStartStr();

    if (s.hoursDate !== today) {
      if (s.lastActiveDate) {
        var diff = Math.round((new Date(today) - new Date(s.lastActiveDate)) / 86400000);
        if (diff > 1) s.streak = 0;
      }
      s.hoursToday = 0;
      s.hoursDate  = today;
    }
    if (s.dsaTodayDate !== today) {
      s.dsaToday     = 0;
      s.dsaTodayDate = today;
    }
    if (s.pomoTodayDate !== today) {
      s.pomoToday     = 0;
      s.pomoTodayDate = today;
    }
    if (s.weeklyHoursDate !== week) {
      s.weeklyHours     = [0, 0, 0, 0, 0, 0, 0];
      s.weeklyHoursDate = week;
    }
    if (s.aptWeekDate !== week) {
      s.aptWeek     = 0;
      s.aptWeekDate = week;
    }
    save(s);
    return s;
  }

  function markStudy(s) {
    var today = SHIBI.Time.todayStr();
    if (s.lastActiveDate !== today) {
      if (s.lastActiveDate) {
        var diff = Math.round((new Date(today) - new Date(s.lastActiveDate)) / 86400000);
        s.streak = (diff === 1) ? s.streak + 1 : 1;
      } else {
        s.streak = 1;
      }
      s.lastActiveDate = today;
    }
    // bump daily activity
    if (!s.dailyActivity) s.dailyActivity = {};
    if (!s.dailyActivity[today]) s.dailyActivity[today] = { hours: 0, problemsSolved: 0, tasksDone: 0, score: 0 };
    return s;
  }

  function bumpActivity(s, field, amount) {
    var today = SHIBI.Time.todayStr();
    if (!s.dailyActivity) s.dailyActivity = {};
    if (!s.dailyActivity[today]) s.dailyActivity[today] = { hours: 0, problemsSolved: 0, tasksDone: 0, score: 0 };
    s.dailyActivity[today][field] = (s.dailyActivity[today][field] || 0) + (amount || 1);
  }

  return { load, save, reset, dailyReset, markStudy, bumpActivity, defaultState };
})();
